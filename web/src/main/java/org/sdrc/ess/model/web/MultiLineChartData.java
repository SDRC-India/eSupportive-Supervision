package org.sdrc.ess.model.web;

/**
 * @author Harsh Pratyush (harsh@sdrc.co.in) This line class contains the data
 *         for multi line chart of dashboard landing page
 */
public class MultiLineChartData {

	String area_Name;
	String key;
	String percentageValue;
	String indicator_Name;
	String short_Name;
	String sourceName;
	String timeperiod;
	String unit_Value;

	public String getArea_Name() {
		return area_Name;
	}

	public void setArea_Name(String area_Name) {
		this.area_Name = area_Name;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getPercentageValue() {
		return percentageValue;
	}

	public void setPercentageValue(String percentageValue) {
		this.percentageValue = percentageValue;
	}

	public String getIndicator_Name() {
		return indicator_Name;
	}

	public void setIndicator_Name(String indicator_Name) {
		this.indicator_Name = indicator_Name;
	}

	public String getShort_Name() {
		return short_Name;
	}

	public void setShort_Name(String short_Name) {
		this.short_Name = short_Name;
	}

	public String getSourceName() {
		return sourceName;
	}

	public void setSourceName(String sourceName) {
		this.sourceName = sourceName;
	}

	public String getTimeperiod() {
		return timeperiod;
	}

	public void setTimeperiod(String timeperiod) {
		this.timeperiod = timeperiod;
	}

	public String getUnit_Value() {
		return unit_Value;
	}

	public void setUnit_Value(String unit_Value) {
		this.unit_Value = unit_Value;
	}

}
