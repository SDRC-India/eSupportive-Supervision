package org.sdrc.ess.model.web;

public class SubmissionWiseDesignationReportModel {
	
	private String stateName;
	private String districtName;
	private String blockName;
	private String organizationName;
	private String designationName;
	private Integer numberOfUsers;
	private Integer numberOfFacilityUsers;
	private String percentageOfFacilityUsers;
	private Integer numberOfCommunityUsers;
	private String percentageOfCommunityUsers;
	
	
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public String getBlockName() {
		return blockName;
	}
	public void setBlockName(String blockName) {
		this.blockName = blockName;
	}
	public String getOrganizationName() {
		return organizationName;
	}
	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}
	public String getDesignationName() {
		return designationName;
	}
	public void setDesignationName(String designationName) {
		this.designationName = designationName;
	}
	public Integer getNumberOfUsers() {
		return numberOfUsers;
	}
	public void setNumberOfUsers(Integer numberOfUsers) {
		this.numberOfUsers = numberOfUsers;
	}
	public Integer getNumberOfFacilityUsers() {
		return numberOfFacilityUsers;
	}
	public void setNumberOfFacilityUsers(Integer numberOfFacilityUsers) {
		this.numberOfFacilityUsers = numberOfFacilityUsers;
	}
	public String getPercentageOfFacilityUsers() {
		return percentageOfFacilityUsers;
	}
	public void setPercentageOfFacilityUsers(String percentageOfFacilityUsers) {
		this.percentageOfFacilityUsers = percentageOfFacilityUsers;
	}
	public Integer getNumberOfCommunityUsers() {
		return numberOfCommunityUsers;
	}
	public void setNumberOfCommunityUsers(Integer numberOfCommunityUsers) {
		this.numberOfCommunityUsers = numberOfCommunityUsers;
	}
	public String getPercentageOfCommunityUsers() {
		return percentageOfCommunityUsers;
	}
	public void setPercentageOfCommunityUsers(String percentageOfCommunityUsers) {
		this.percentageOfCommunityUsers = percentageOfCommunityUsers;
	}
	
	

}
