package org.sdrc.ess.model.web.designation;

public class DesignationMangementTableModel {

	private String userLevel;
	private String stateName;
	private String organizationName;
	private String designationName;
	private String responsibleFacility;
	private String responsibleCommunity;
	private String isFacilityInCharge;

	public String getUserLevel() {
		return userLevel;
	}

	public void setUserLevel(String userLevel) {
		this.userLevel = userLevel;
	}

	public String getStateName() {
		return stateName;
	}

	public void setStateName(String stateName) {
		this.stateName = stateName;
	}

	public String getOrganizationName() {
		return organizationName;
	}

	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}

	public String getDesignationName() {
		return designationName;
	}

	public void setDesignationName(String designationName) {
		this.designationName = designationName;
	}

	public String getResponsibleFacility() {
		return responsibleFacility;
	}

	public void setResponsibleFacility(String responsibleFacility) {
		this.responsibleFacility = responsibleFacility;
	}

	public String getResponsibleCommunity() {
		return responsibleCommunity;
	}

	public void setResponsibleCommunity(String responsibleCommunity) {
		this.responsibleCommunity = responsibleCommunity;
	}

	public String getIsFacilityInCharge() {
		return isFacilityInCharge;
	}

	public void setIsFacilityInCharge(String isFacilityInCharge) {
		this.isFacilityInCharge = isFacilityInCharge;
	}

}
